#Strings
course = 'Python for Beginners'
#indexs - 012345 678 9 etc
print(course.upper()) #ALL UPPERCASE
print(course.lower()) #all lowercase
print(course.find('y')) #show the index of where y is at the sentence (Caps Sensitive)
print(course.replace('for', '4')) #replaces for with 4
print('Python' in course) #same as .find but instead say true or false