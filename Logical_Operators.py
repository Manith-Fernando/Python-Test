#Logical Operators
price = 25
print(price > 10 and price < 30) # is 25 between 10 and 30 (True or False)

price = 5
print(price > 10 or price < 30) # is 5 more that 10 or less than 30 (True or False)

price = 5
print(not price > 10) # turns true to false or false to true

